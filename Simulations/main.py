"""
main.py — RNA-seq simulation pipeline entry point.

What it does:
    Orchestrates the full 3-phase RNA-seq simulation pipeline:
      Phase 1 : prepare_reference.py   — GTF + genome → simulation-ready reference files
      Phase 2 : generate_abundances.py — synthetic TPM abundance profile
      Phase 3a: simulate_illumina.py   — paired-end Illumina reads via RSEM
      Phase 3b: simulate_pacbio.py     — PacBio CCS reads via IsoSeqSim
      Phase 3c: simulate_ont.py        — ONT reads via Trans-NanoSim

    All configuration is passed via CLI arguments (see run.sh).
    Default values are defined in the DEFAULTS block below.
    Outputs land in a timestamped folder: files/results/exprmnt_{timestamp}/

Inputs (pass via CLI or set defaults below):
    --genome        GRCh38 genome FASTA
    --gtf           GENCODE GTF annotation
    --rsem_model    RSEM pre-trained model file (Illumina only)
    --abundance_mode  lognormal | custom | uniform

Outputs (files/results/exprmnt_{timestamp}/):
    experiment_description.log  — full config dump + what/why/expected outcome
    running.log                 — stdout (first line: Script: <path>)
    ref_data/                   — Phase 1 reference files
    abundances.tsv              — TPM profile (ground truth expression)
    Illumina.simulated_1.fq     — Illumina paired-end reads (forward)
    Illumina.simulated_2.fq     — Illumina paired-end reads (reverse)
    PacBio.simulated.fasta      — PacBio CCS reads
    ONT.simulated.fastq         — ONT reads
    *.isoform_counts.tsv        — per-technology ground-truth read counts
    *.read_to_isoform.tsv       — read ID → isoform mapping
    results_summary.txt         — read count totals and isoform coverage stats
    runtime.txt                 — total wall-clock time
    code_snapshot/              — exact copy of code/Simulations/ at run time

Estimated runtime (full run, 16 threads):
    Phase 1 — prepare reference     : ~TBD
    Phase 2 — generate abundances   : ~TBD
    Phase 3a — Illumina (100M reads): ~TBD
    Phase 3b — PacBio  (10M reads)  : ~TBD
    Phase 3c — ONT     (30M reads)  : ~TBD
    Total                           : ~TBD

Run:
    cd /gpfs/commons/home/atalukder/RNA_Splicing
    bash code/Simulations/run.sh          # recommended — set all options in run.sh

    OR call directly (example):
    python code/Simulations/main.py \\
        --genome  data/ReqToSimulateData/human.genome.fasta \\
        --gtf     data/ReqToSimulateData/human.annotation.gtf \\
        --pacbio_count 1000 --test_mode
"""

